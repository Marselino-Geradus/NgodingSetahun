#Tampilkan elemen pertama dari list berikut
puluhan = range(10,40+1,10)
a = list(puluhan)
print(a)
print('Elemen pertama (indeks 0) = ',a[0])
print('='*5)

#Elemen Terakhir
print(a)
print('Elemen terakhir (indeks -1) = ',a[-1])
print('='*5)

#Elemen di indeks ke-1 sampai 3
print(a)
print('Elemen di indeks 1-3 = ',a[1:4])